package sesameStreet;
public class SesameStreet {
	
	public void oneHaHaHa() {
		System.out.println("----------------"
				+ "\n#4 Counting with the Count!\n");
		
		int count;
		
		for(count=1;
			count<6;
			count++)
				System.out.print(count+"... ");
		
		System.out.println("Complete!");
		
		System.out.println("----------------");
	}
	
}
